/** فرمت تاریخ به فارسی */
export function formatDateFa(dateStr: string): string {
  try {
    const date = new Date(dateStr + 'T00:00:00');
    const day = date.getDate();
    const month = fa.months[date.getMonth()];
    const year = date.getFullYear();
    return `${day} ${month} ${year}`;
  } catch {
    return dateStr;
  }
}

/** فرمت تاریخ کامل با روز هفته */
export function formatDateFullFa(dateStr: string): string {
  try {
    const date = new Date(dateStr + 'T00:00:00');
    const dayOfWeek = fa.weekdaysFull[date.getDay()];
    const day = date.getDate();
    const month = fa.months[date.getMonth()];
    const year = date.getFullYear();
    return `${dayOfWeek}، ${day} ${month} ${year}`;
  } catch {
    return dateStr;
  }
}

/** تبدیل تاریخ به رشته YYYY-MM-DD */
export function toDateStr(date: Date): string {
  return date.toISOString().split('T')[0];
}
